let c = 5, d = 10;
console.log(c < d); // 작다: true
console.log(c > d); // 크다: false
console.log(c <= 5); // 작거나 같다: true
console.log(c >= 5); // 크거나 같다: true
console.log(c == "5"); // 동등: true (타입은 비교하지 않음)
console.log(c === "5"); // 일치: false (타입도 비교)
console.log(c != "10"); // 부등: true
console.log(c !== "5"); // 불일치: true
