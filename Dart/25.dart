void main() {
    // bool 형
    bool isTrue = true;

    // int 형
    int number1 = 1;

    // double 형
    double number2 = 1.0;

    // string 형
    String text = "Hello World";

    // null 형
    Null thisNull = null;

    print(isTrue);
    print(number1);
    print(number2);
    print(text);
    print(thisNull);

    // 가변형 var
    var value1 = 1;
    value1 = 2;

    // 가변형 dynamic
    dynamic value2 = 2;
    value2 = "3";

    print(value1);
    print(value2);
}