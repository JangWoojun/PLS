void main() {
  String fruit = 'apple';

  switch (fruit) {
    case 'apple':
      print('Selected fruit is apple.');
      break;

    case 'banana':
      print('Selected fruit is banana.');
      break;

    case 'orange':
      print('Selected fruit is orange.');
      break;

    default:
      print('Selected fruit is not apple, banana, or orange.');
      break;
  }
}
